DWORD get_fattime (void)
{
	struct timeval tv;
	gettimeofday(&tv, NULL);

	struct tm *t = localtime((time_t *)&tv.tv_sec);
	return    ((t->tm_year - 80) << 25)
			| ((t->tm_mon + 1) << 21)
			| (t->tm_mday << 16)
			| (t->tm_hour << 11)
			| (t->tm_min << 5)
			| (t->tm_sec >> 1);
}

