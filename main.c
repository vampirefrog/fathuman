DRESULT disk_read (BYTE pdrv, BYTE* buff, DWORD sector, UINT count) {
	if(sector == 0) { // Fake boot sector here
		memcpy(buff, fake_bootsect, 1024);
	} else {
		fseek(xdfp, sector * _MIN_SS + offset, SEEK_SET);
		fread(buff, _MIN_SS, count, xdfp);
	}
	return 0;
}
